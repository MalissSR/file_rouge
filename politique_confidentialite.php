    <h1>Politique de confidentialité</h1>
    <p>
        Votre vie privée est importante pour nous. Cette politique de confidentialité explique quelles informations personnelles nous collectons et comment nous les utilisons.
    </p>
    <h2>Données personnelles collectées</h2>
    <p>Nous collectons les données suivantes :</p>
    <ul>
        <li>Nom et prénom</li>
        <li>Adresse e-mail</li>
        <li>Adresse physique</li>
        <li>Numéro de téléphone</li>
        <li>Adresse IP</li>
        <li>Données de localisation</li>
        <li>Cookies et autres technologies similaires</li>
    </ul>
    <h2>Finalités de la collecte de données</h2>
    <p>Nous collectons vos données personnelles pour les finalités suivantes :</p>
    <ul>
        <li>Traitement des commandes</li>
        <li>Communication marketing</li>
        <li>Amélioration de nos services</li>
    </ul>
    <h2>Durée de conservation des données</h2>
    <p>Nous conservons vos données personnelles aussi longtemps que nécessaire pour atteindre les finalités pour lesquelles elles ont été collectées, à moins qu'une durée de conservation plus longue ne soit requise ou autorisée par la loi.</p>
    <p>Dans certains cas, la durée de conservation est fixée par la règlementation, mais pour d'autres traitements de données, elle est déterminée en fonction de la finalité du traitement.</p>
    <p>Pour plus d'informations sur les durées de conservation des données, consultez <a href="https://www.cnil.fr/fr/les-durees-de-conservation-des-donnees">cette page</a>.</p>
